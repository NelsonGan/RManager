function openDrop(){
  if (document.getElementById('todrop').style.display == "flex"){
    document.getElementById('todrop').style.display = "none";
  } else {
    document.getElementById('todrop').style.display = "flex";
  }
}

function Dropdown(){
  if (document.getElementById('drop').style.display == "flex"){
    document.getElementById('drop').style.display = "none";
  } else {
    document.getElementById('drop').style.display = "flex";
  }
}